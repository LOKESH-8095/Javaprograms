package ex_13_Stringbuilder_Stringbuffer_functions;

public class Lab_63_StringBuilder_Buffer {
    public static void main(String[] args)
    {
        String a = "Lokesh";
        String b = new String ("Shetty");

        StringBuffer stringBuffer = new StringBuffer("Loki");
        StringBuilder stringBuilder = new StringBuilder("Loki");

        System.out.println(a);
        System.out.println(b);
        System.out.println(stringBuffer);
        System.out.println(stringBuilder);
    }
}
